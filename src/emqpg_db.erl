%% @doc
%% 合宙定位信息入库
%% @end
db_pg_gnss(Topic, {Lng, Lat}) ->
    try
        emqpg_pgpool:equery("insert into lc_hzgnss(topic, lng, lat)
        values($1, $2, $3);", [Topic, Lng, Lat])
    catch
        Exception:Error -> 
            lager:error("Database Insert Failed: ~p:~p", [Exception, Error])
    end.

